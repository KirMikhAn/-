#include <iostream>
#include <cmath>

int main() {
    double num1 = 14.0;
    double num2 = 16.0;
    double num3 = 23.0;

    std::cout << "Квадратный корень из " << num1 << ": " << sqrt(num1) << '\n';
    std::cout << "Квадратный корень из " << num2 << ": " << sqrt(num2) << '\n';
    std::cout << "Квадратный корень из " << num3 << ": " << sqrt(num3) << '\n';

    return 0;
}
